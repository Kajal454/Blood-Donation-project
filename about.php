<p>
 Project has proposed a system which not only maintains the information of blood banks, 
 but also maintains information of blood camps which makes blood transfusion process easier. 
 This online blood donation management system maintains the list of blood donors and also helps the recipients to track and search the right donor easily. 
 It has two modules namely Admin and User. Admins can add hospitals having blood banks and can also add various blood donation camps. 
 He/she can also view the list of donors of a particular area with proper Blood cross match. 
 He/she can also check for blood requests and in case of emergency he/she can send notifications to blood donors as per the requirements. 
 Users can register and make a request. Users can also register as a donor. 
 Donors can check for Blood camps and hospitals for blood donation and will be getting notifications in case of emergency. 
 They can either accept or ignore it. 
 This project aims at maintaining all information regarding blood donors, 
 different blood groups available in blood banks as wells as blood camps and help them manage in a better way.
